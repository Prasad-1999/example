# Interger conversions
# int to float
a = 42
print(float(a))

# int to string
b = 456
print(str(b))

# int to boolean
c = 22
print(bool(c))

# String conversions
# str to int
d = '22'
print(int(d))

# str to float
e = '2234'
print(float(e))

# str to boolean
f = '22345'
print(bool(f))

# Boolean conversions
# boolean to int
g = '1'
print(int(g))

# boolean to float
h = '1'
print(float(h))

# boolean to str
i = 1
print(str(i))

# float conversion
# float to int
j = 25.67
print(int(j))

# float to boolean
k = 25
print(bool(k))

# float to str
l = 22.46
print(str(l))
