# DDL:
'''
1.DDL means the data defination language 
2.It is mainly consists of four operations like create,drop,alter,truncate.
'''
# Create:
"To create the database table"
# Drop:
"To removing the database table"
# Alter:
"To update the existing data in table"
# Truncate:
"To remove all rows from existing table"

# DML:
'''
1.DML means the data manipulate language
2.It also consists os four operations like select,update,Delete,Insert
'''
# Select:
"To retrieve (or) download the data from table"
# Update:
"To add the new data to table"
# Delete:
"To delete data from table"
# Insert:
"To add the row in to table"
